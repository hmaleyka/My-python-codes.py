# Accessing tuple elements using slicing
my_tuple = ('u','n','i','v','e','r','s','i','t', 'y')

# elements 2nd to 4th

print(my_tuple[1:4])

# elements beginning to 2nd

print(my_tuple[:-7])

# elements 8th to end

print(my_tuple[7:])

# elements beginning to end

print(my_tuple[:])